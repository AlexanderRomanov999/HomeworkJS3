/*3
Пользователь с клавиатуры вводит 3 числа, необходимо создать функцию, которая определяет максимальное значение среди этих чисел*/

function Max (num1, num2, num3) {
    if (num1 >= num2 && num1 >= num3) {
        return num1;
    }
    else if (num2 >= num1 && num2 >= num3) {
        return num2;
    }
    else if (num3 >= num1 && num3 >= num2) {
        return num3;
    }
}
let first = Number(prompt('Введите первое число'));
let second = Number(prompt('Введите второе число'));
let third = Number(prompt('Введите третье число'));
console.log("Максимальное значение среди чисел: " + Max(first, second, third));

