function Sum (num1, num2) {
    return num1 + num2;
}
function Dif (num1, num2) {
    if (num1 >= num2) {
        return num1 - num2;
    }
    else {
        return num2 - num1;
    }
}
function Mul (num1, num2) {
    return num1 * num2;
}
function Div (num1, num2) {
    return num1 / num2;
}
console.log(Sum(2, 6)); // 8
console.log(Dif(10, 15)); // 5
console.log(Mul(24, 3.5)); // 84
console.log(Div(2023, 7)); // 289